static void d_gfx_put_ex(int x, int y, color c, d_blend blend) {
	d_img *img = d_gfx.cur_canvas;
	if (x < 0 || x >= img->width || y < 0 || y >= img->height) {
		return;
	}
	int i = (int)(y * img->width + x);
	switch (blend) {
		case D_BLEND_ALPHA: {
			if (c.a == 255) {
				img->pixels[i] = c;
			} else if (c.a != 0) {
				color rc = img->pixels[i];
				img->pixels[i] = (color) {
					.r = (rc.r * (255 - c.a) + c.r * c.a) / 255,
					.g = (rc.g * (255 - c.a) + c.g * c.a) / 255,
					.b = (rc.b * (255 - c.a) + c.b * c.a) / 255,
					.a = (rc.a * (255 - c.a) + c.a * c.a) / 255,
				};
			}
			break;
		}
		case D_BLEND_REPLACE:
			img->pixels[i] = c;
			break;
		case D_BLEND_ADD:
			if (c.a != 0) {
				color rc = img->pixels[i];
				img->pixels[i] = (color) {
					.r = (rc.r * rc.a + c.r * c.a) / 255,
					.g = (rc.g * rc.a + c.g * c.a) / 255,
					.b = (rc.b * rc.a + c.b * c.a) / 255,
					.a = (rc.a * rc.a + c.a * c.a) / 255,
				};
			}
			break;
	}
}

void d_gfx_put(int x, int y, color c) {
	d_gfx_put_ex(x, y, c, d_gfx.blend);
}

color d_gfx_seek_ex(int x, int y, d_wrap wrap) {
	d_img *img = d_gfx.cur_canvas;
	switch (wrap) {
		case D_WRAP_BORDER:
			if (x < 0 || x >= img->width || y < 0 || y >= img->height) {
				return colori(0, 0, 0, 0);
			} else {
				return img->pixels[(int)(y * img->width + x)];
			}
		case D_WRAP_EDGE: {
			int xx = clampi(x, 0, img->width - 1);
			int yy = clampi(y, 0, img->height - 1);
			return img->pixels[(int)(yy * img->width + xx)];
		}
		case D_WRAP_REPEAT: {
			int xx = x % img->width;
			int yy = y % img->height;
			return img->pixels[(int)(yy * img->width + xx)];
		}
	}
}

color d_gfx_seek(int x, int y) {
	return d_gfx_seek_ex(x, y, d_gfx.wrap);
}

void d_draw_img(const d_img *img, vec2 pos) {
	for (int x = 0; x < img->width; x++) {
		for (int y = 0; y < img->height; y++) {
			d_gfx_put(x + pos.x, y + pos.y, img->pixels[y * img->width + x]);
		}
	}
}

void d_draw_tri(vec2 p1, vec2 p2, vec2 p3, color c) {

	if (p1.y > p2.y) {
		d_draw_tri(p2, p1, p3, c);
		return;
	}

	if (p1.y > p3.y) {
		d_draw_tri(p3, p2, p1, c);
		return;
	}

	if (p2.y > p3.y) {
		d_draw_tri(p1, p3, p2, c);
		return;
	}

	int x1 = p1.x;
	int y1 = p1.y;
	int x2 = p2.x;
	int y2 = p2.y;
	int x3 = p3.x;
	int y3 = p3.y;

	for (int y = y1; y < y3; y++) {
		int x_start = y < y2
			? mapi(y, y1, y2, x1, x2)
			: mapi(y, y2, y3, x2, x3);
		int x_end = mapi(y, y1, y3, x1, x3);
		if (x_start > x_end) {
			swapi(&x_start, &x_end);
		}
		for (int x = x_start; x < x_end; x++) {
			d_gfx_put(x, y, c);
		}
	}

}

void d_draw_textri(vec3 p1, vec2 uv1, vec3 p2, vec2 uv2, vec3 p3, vec2 uv3, d_img *tex) {

	if (p1.y > p2.y) {
		d_draw_textri(p2, uv2, p1, uv1, p3, uv3, tex);
		return;
	}

	if (p1.y > p3.y) {
		d_draw_textri(p3, uv3, p2, uv2, p1, uv1, tex);
		return;
	}

	if (p2.y > p3.y) {
		d_draw_textri(p1, uv1, p3, uv3, p2, uv2, tex);
		return;
	}

	int x1 = p1.x;
	int y1 = p1.y;
	float z1 = p1.z;
	int x2 = p2.x;
	int y2 = p2.y;
	float z2 = p2.z;
	int x3 = p3.x;
	int y3 = p3.y;
	float z3 = p3.z;

	for (int y = y1; y < y3; y++) {

		bool prebend = y < y2;

		int x_start = prebend
			? mapi(y, y1, y2, x1, x2)
			: mapi(y, y2, y3, x2, x3);
		int x_end = mapi(y, y1, y3, x1, x3);

		float z_start = prebend
			? mapf(y, y1, y2, z1, z2)
			: mapf(y, y2, y3, z2, z3);
		float z_end = mapf(y, y1, y3, z1, z3);

		if (x_start > x_end) {
			swapi(&x_start, &x_end);
		}

		float len = x_end - x_start;

		vec2 uv_start = prebend
			? vec2_lerp(uv1, uv2, (float)(y - y1) / (float)(y2 - y1))
			: vec2_lerp(uv2, uv3, (float)(y - y2) / (float)(y3 - y2));
		vec2 uv_end = vec2_lerp(uv1, uv3, (float)(y - y1) / (float)(y3 - y1));

		for (int x = x_start; x < x_end; x++) {
			vec2 uv = vec2_lerp(uv_start, uv_end, (x - x_start) / len);
			float z = mapf(x, x_start, x_end, z_start, z_end);
			if (d_fbuf_get(&d_gfx.depth_buf, x, y) < z) {
				d_fbuf_set(&d_gfx.depth_buf, x, y, z);
				color c = d_img_get(tex, tex->width * uv.x, tex->height * uv.y);
				d_gfx_put(x, y, c);
			}
		}

	}

}

void d_draw_rect(vec2 p1, vec2 p2, color c) {

	int x1 = p1.x < p2.x ? p1.x : p2.x;
	int x2 = p1.x > p2.x ? p1.x : p2.x;
	int y1 = p1.y < p2.y ? p1.y : p2.y;
	int y2 = p1.y > p2.y ? p1.y : p2.y;

	for (int x = x1; x < x2; x++) {
		for (int y = y1; y < y2; y++) {
			d_gfx_put(x, y, c);
		}
	}

}

void d_draw_circle(vec2 center, float r, color c) {

	for (int i = center.x - r; i <= center.x + r; i++) {
		for (int j = center.y - r; j <= center.y + r; j++) {
			vec2 p = vec2f(i, j);
			float d = vec2_dist(p, center);
			if (d <= r) {
				d_gfx_put(p.x, p.y, c);
			}
		}
	}

}

void d_draw_text(const char *text, vec2 pos, color  c) {

	int num_chars = strlen(text);
	d_font *font = &d_gfx.def_font;
	int rw = font->gw * font->cols;
	int ox = 0;

	for (int i = 0; i < num_chars; i++) {

		int ii = font->map[(int)text[i]];
		int xx = ii % font->cols * font->gw;
		int yy = ii / font->cols * font->gh;

		for (int x = 0; x < font->gw; x++) {
			for (int y = 0; y < font->gh; y++) {
				if (font->pixels[(yy + y) * rw + xx + x] == 1) {
					d_gfx_put(pos.x + ox + x, pos.y + y, c);
				}
			}
		}

		ox += font->gw;

	}

}

void d_draw_line(vec2 p1, vec2 p2, color c) {

	int x1 = p1.x;
	int y1 = p1.y;
	int x2 = p2.x;
	int y2 = p2.y;
	bool steep = abs(y2 - y1) > abs(x2 - x1);

	if (steep) {
		swapi(&x1, &y1);
		swapi(&x2, &y2);
	}

	if (x1 > x2) {
		swapi(&x1, &x2);
		swapi(&y1, &y2);
	}

	int dx = x2 - x1;
	int dy = abs(y2 - y1);
	int err = dx / 2;
	int step = y1 < y2 ? 1 : -1;
	int y = y1;

	for (int x = x1; x <= x2; x++) {
		if (steep) {
			d_gfx_put(y, x, c);
		} else {
			d_gfx_put(x, y, c);
		}
		err -= dy;
		if (err < 0) {
			y += step;
			err += dx;
		}
	}

}

void d_gfx_t_push() {
	if (d_gfx.num_t >= MAX_TSTACK) {
		fprintf(stderr, "tstack overflow\n");
		return;
	}
	d_gfx.tstack[d_gfx.num_t++] = d_gfx.t;
}

void d_gfx_t_pop() {
	if (d_gfx.num_t <= 0) {
		fprintf(stderr, "tstack underflow\n");
		return;
	}
	d_gfx.t = d_gfx.tstack[--d_gfx.num_t];
}

void d_gfx_t_use(mat4 m) {
	d_gfx.t = mat4_mult(d_gfx.t, m);
}

void d_gfx_t_move(vec3 p) {
	d_gfx.t = mat4_mult(d_gfx.t, mat4_translate(p));
}

void d_gfx_t_scale(vec3 s) {
	d_gfx.t = mat4_mult(d_gfx.t, mat4_scale(s));
}

void d_gfx_t_rot_x(float a) {
	d_gfx.t = mat4_mult(d_gfx.t, mat4_rot_x(a));
}

void d_gfx_t_rot_y(float a) {
	d_gfx.t = mat4_mult(d_gfx.t, mat4_rot_y(a));
}

void d_gfx_t_rot_z(float a) {
	d_gfx.t = mat4_mult(d_gfx.t, mat4_rot_z(a));
}

mat4 d_psr_mat4(d_psr *psr) {
	return mat4_mult(
		mat4_mult(
			mat4_translate(psr->pos),
			mat4_scale(psr->scale)
		),
		mat4_rot_quat(psr->rot)
	);
}

static void d_draw_model_node(d_model *model, d_model_node *node) {
	d_gfx_t_push();
	d_gfx_t_use(d_psr_mat4(&node->psr));
	for (int i = 0; i < node->num_meshes; i++) {
		d_mesh *mesh = &node->meshes[i];
		for (int j = 0; j < mesh->num_indices; j += 3) {
			d_vertex *v1 = &mesh->verts[mesh->indices[j + 0]];
			d_vertex *v2 = &mesh->verts[mesh->indices[j + 1]];
			d_vertex *v3 = &mesh->verts[mesh->indices[j + 2]];
			vec3 p1 = mat4_mult_vec3(d_gfx.t, v1->pos);
			vec3 p2 = mat4_mult_vec3(d_gfx.t, v2->pos);
			vec3 p3 = mat4_mult_vec3(d_gfx.t, v3->pos);
			if (model->num_images > 0) {
				d_draw_textri(
					p1, v1->uv,
					p2, v2->uv,
					p3, v3->uv,
					&model->images[0]
				);
			}
		}
	}
	for (int i = 0; i < node->num_children; i++) {
		d_draw_model_node(model, &node->children[i]);
	}
	d_gfx_t_pop();
}

void d_draw_model(d_model *model) {
	d_gfx_t_push();
	for (int i = 0; i < model->num_nodes; i++) {
		d_draw_model_node(model, &model->nodes[i]);
	}
	d_gfx_t_pop();
}

