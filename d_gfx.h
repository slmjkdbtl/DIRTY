d_model d_load_model(const char *path) {
	size_t size;
	uint8_t *bytes = d_read_bytes(path, &size);
	if (!bytes) {
		fprintf(stderr, "failed to read '%s'\n", path);
		return d_model_empty();
	}
	d_model model = d_parse_model(bytes, size);
	free(bytes);
	return model;
}
